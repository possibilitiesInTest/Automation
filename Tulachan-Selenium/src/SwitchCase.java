public class SwitchCase {
    public static void main (String[] args) {
        String grade = "B"; // Student's grade
        switch(grade){
            case "A":
                System.out.println("Great Student");
                break;
            case "B":
                System.out.println("Good Student");
                break;
            case "C":
                System.out.println("Medium Student");
                break;
            case "D":
                System.out.println("Bad Student");
                break;
            case "F":
                System.out.println("Gread Student");
                break;
        }
    }
}

